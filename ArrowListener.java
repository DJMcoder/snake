
/**
 * Contains the interface of ArrowListener class.
 * 
 * @author  Jin Kim
 * @version 8 December 2015
 */
public interface ArrowListener
{
    /**
     * Up is pressed.
     */
    void upPressed();

    /**
     * Down is pressed.
     */
    void downPressed();

    /**
     * Left is pressed.
     */
    void leftPressed();

    /**
     * Right is pressed.
     */
    void rightPressed();
}